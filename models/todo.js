var mongoose = require('mongoose');

var Todoschema = mongoose.Schema( {
    text: {
        type: String,
        required: true,
        minlength: 1,
        trim: true
    },
    completed: {
        type: Boolean,
        default: false
    },
    completedAt: {
        type: Number,
        default: null
    },
    _creator: {
        type: mongoose.Schema.Types.ObjectId,
        required: true
    }
});

const Todo = module.exports = mongoose.model('Todo', Todoschema);


